 // Este programa utiliza um loop while para imprimir os números de 1 a 10.

fun main() {
    var numero = 1 // Inicializa a variável número com 1

    // Usa um loop while para imprimir os números de 1 a 10
    while (numero <= 10) {
        println(numero) // Imprime o número atual
        numero++ // Incrementa o valor da variável número
    }
}

